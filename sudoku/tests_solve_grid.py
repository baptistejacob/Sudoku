import unittest

from sudoku.solve import solve


class TestSolveGrid(unittest.TestCase):
    def test_solve_simple_grid(self):
        grid = [
            [0, 0, 4, 0, 5, 0, 0, 0, 0],
            [9, 0, 0, 7, 3, 4, 6, 0, 0],
            [0, 0, 3, 0, 2, 1, 0, 4, 9],
            [0, 3, 5, 0, 9, 0, 4, 8, 0],
            [0, 9, 0, 0, 0, 0, 0, 3, 0],
            [0, 7, 6, 0, 1, 0, 9, 2, 0],
            [3, 1, 0, 9, 7, 0, 2, 0, 0],
            [0, 0, 9, 1, 8, 2, 0, 0, 3],
            [0, 0, 0, 0, 6, 0, 1, 0, 0],
        ]

        solve(grid)

        excepted_grid = [
            [2, 6, 4, 8, 5, 9, 3, 1, 7],
            [9, 8, 1, 7, 3, 4, 6, 5, 2],
            [7, 5, 3, 6, 2, 1, 8, 4, 9],
            [1, 3, 5, 2, 9, 7, 4, 8, 6],
            [8, 9, 2, 5, 4, 6, 7, 3, 1],
            [4, 7, 6, 3, 1, 8, 9, 2, 5],
            [3, 1, 8, 9, 7, 5, 2, 6, 4],
            [6, 4, 9, 1, 8, 2, 5, 7, 3],
            [5, 2, 7, 4, 6, 3, 1, 9, 8],
        ]
        self.assertEqual(grid, excepted_grid)

    def test_solve_hard_grid(self):
        grid = [
            [0, 0, 0, 0, 3, 2, 0, 6, 8],
            [0, 0, 0, 5, 0, 8, 0, 0, 3],
            [0, 0, 0, 7, 0, 4, 0, 0, 1],
            [4, 3, 5, 0, 7, 0, 0, 0, 2],
            [0, 0, 6, 0, 0, 9, 0, 8, 0],
            [0, 0, 0, 0, 0, 0, 3, 0, 0],
            [1, 9, 0, 0, 0, 0, 0, 5, 0],
            [0, 0, 0, 9, 0, 0, 0, 0, 0],
            [5, 0, 4, 0, 8, 0, 7, 0, 9],
        ]

        solve(grid)

        excepted_grid = [
            [7, 4, 9, 1, 3, 2, 5, 6, 8],
            [6, 1, 2, 5, 9, 8, 4, 7, 3],
            [3, 5, 8, 7, 6, 4, 9, 2, 1],
            [4, 3, 5, 8, 7, 1, 6, 9, 2],
            [2, 7, 6, 3, 4, 9, 1, 8, 5],
            [9, 8, 1, 2, 5, 6, 3, 4, 7],
            [1, 9, 3, 4, 2, 7, 8, 5, 6],
            [8, 6, 7, 9, 1, 5, 2, 3, 4],
            [5, 2, 4, 6, 8, 3, 7, 1, 9],
        ]
        self.assertEqual(grid, excepted_grid)
